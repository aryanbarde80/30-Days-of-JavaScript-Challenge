// Using Function Expression

const Concat = function(str1,str2) {
    return str1+str2;
  };

console.log(Concat(`Code with`,' Harry'));  
console.log(Concat(`Carry`," Minati"));  


// Using Arrow Function

const Concatenate = (str1,str2) => {
    return str1+str2;
  };
  
console.log(Concatenate(`Aryan.`,`Barde`));  
console.log(Concatenate(`Vicky.`,`Kaushal`)); 
  