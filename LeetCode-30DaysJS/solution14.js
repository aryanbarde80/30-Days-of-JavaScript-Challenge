/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // if cancelFn executed before fn, then cancel the execution of fn

    // We will use a timeout function, it returns a unique timeout id

    const timeoutId = setTimeout (() => {
        fn(...args)
    },t)
    const cancelFn = () => {
        clearTimeout(timeoutId)
    }
    return cancelFn;
};

